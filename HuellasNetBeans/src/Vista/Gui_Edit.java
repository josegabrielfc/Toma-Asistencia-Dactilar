    private void txtReemplazarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtReemplazarMouseClicked
        JTextField buscarField = new JTextField(10);
        JTextField reemplazarField = new JTextField(10);

        JPanel myPanel = new JPanel();
        myPanel.add(new JLabel("Buscar:"));
        myPanel.add(buscarField);
        myPanel.add(Box.createHorizontalStrut(25)); // un espaciador
        myPanel.add(new JLabel("Reemplazar: "));
        myPanel.add(reemplazarField);

        int result = JOptionPane.showConfirmDialog(null, myPanel,
                "Ingrese los siguientes datos", JOptionPane.OK_CANCEL_OPTION);
        if (result == JOptionPane.OK_OPTION) {
            String buscar = buscarField.getText();
            String reemplazar = reemplazarField.getText();

            //System.out.println("Buscar:" + buscar);
            //System.out.println("Reemplazar: " + reemplazar);
            EditUFPS texto = new EditUFPS(this.txtDocumento.getText());
            int totalReemplazos = texto.reemplazar(buscar, reemplazar);
            this.txtDocumento.setText(texto.listaToString());
            JOptionPane.showMessageDialog(null, "Se realizaron: " + totalReemplazos + " reemplazos", "Edit_UFPS",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            emptyDocumentError();
        }
    }//GEN-LAST:event_txtReemplazarMouseClicked

    private void txtEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtEliminarMouseClicked
        if (!editufps.documentEmpty()) {
            String deleteText = JOptionPane.showInputDialog(null, "Palabra a eliminar", "Edit_UFPS", JOptionPane.QUESTION_MESSAGE);
            EditUFPS texto = new EditUFPS(this.txtDocumento.getText());
            int cantidadEliminada = texto.eliminar(deleteText);
            this.txtDocumento.setText(texto.listaToString());
            JOptionPane.showMessageDialog(null, "Se realizaron: " + cantidadEliminada + " eliminaciones", "Edit_UFPS",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            emptyDocumentError();
        }
    }//GEN-LAST:event_txtEliminarMouseClicked

    private void txtPDFMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtPDFMouseClicked
        if (!editufps.documentEmpty()) {
            String nombre = JOptionPane.showInputDialog(null, "Nombre del archivo pdf", "Edit_UFPS", JOptionPane.QUESTION_MESSAGE);
            if ((nombre != null) && (nombre.length() > 0)) {
                try {
                    editufps.crearPDF(nombre, txtDocumento.getText());
                } catch (DocumentException | FileNotFoundException ex) {
                    Logger.getLogger(Gui_Edit.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else
            emptyDocumentError();
    }//GEN-LAST:event_txtPDFMouseClicked

    private void txtPDFSubrayadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtPDFSubrayadoMouseClicked
        if (!editufps.documentEmpty()) {
            JTextField nombreArchivo = new JTextField(10);
            JTextField palabraSubrayar = new JTextField(10);

            JPanel myPanel = new JPanel();
            myPanel.add(new JLabel("Nombre del archivo:"));
            myPanel.add(nombreArchivo);
            myPanel.add(Box.createHorizontalStrut(35)); // un espaciador
            myPanel.add(new JLabel("Palabra a Subrayar: "));
            myPanel.add(palabraSubrayar);

            int result = JOptionPane.showConfirmDialog(null, myPanel,
                    "Ingrese los siguientes datos", JOptionPane.OK_CANCEL_OPTION);

            EditUFPS texto = new EditUFPS(this.txtDocumento.getText());

            texto.buscarYSubrayar(palabraSubrayar.getText());

            String name = nombreArchivo.getText();
            if ((name != null) && (name.length() > 0)) {
                try {
                    editufps.crearPDF(name, txtDocumento.getText());
                } catch (DocumentException | FileNotFoundException ex) {
                    Logger.getLogger(Gui_Edit.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            emptyDocumentError();
        }


    }//GEN-LAST:event_txtPDFSubrayadoMouseClicked

