    private void mostrarContenidoURL(String url) {
        ArchivoLeerURL archivo = new ArchivoLeerURL(url);
        Object datos[] = archivo.leerArchivo();
        String texto = "";
        for (Object x : datos) {
            texto = x.toString() + "\n";
        }
        this.txtDocumento.setText(texto);
    }

    private void mostrarContenidoDocumento(String ruta) {

        ArchivoLeerDocumento archivo = new ArchivoLeerDocumento(ruta);

        Object datos[] = (Object[]) archivo.leer(ruta);
        String texto = "";
        for (Object x : datos) {
            texto = x.toString() + "\n";
        }
        this.txtDocumento.setText(texto);
    }

